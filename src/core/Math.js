// Math Class
var Math = {};

// Methods (organize methods by function and relationship)
Math.add = function( a, b ) {

	return a + b;

}

Math.subtract = function( a, b ) {

	return a - b;

}

Math.multiply = function( a, b) {

	return a * b;

}

Math.divide = function( a, b ) {

	// a = the dividend
	// b = the divisor

	return a / b;

}

Math.modulo = function( a, b ) {

	// a = the dividend
	// b = the divisor
	// a % b = the remainder from a / b

	return a % b;

}

// End Methods

// End Class